from typing import List


class Solution:
    def minDominoRotations(self, A: List[int], B: List[int]) -> int:
        UP = A[0]
        DOWN = B[0]
        a_rotations = 0
        a_discarded = False
        b_rotations = 0
        b_discarded = False
        for idx in range(1, len(A)):
            #A  1   -   2
            #B  2   -   2
            #
            if (A[idx] == UP or A[idx] == DOWN) or (B[idx] == UP or B[idx] == DOWN):
                if A[idx] == DOWN and B[idx] != DOWN:
                    b_rotations += 1
                elif B[idx] == UP and A[idx] != UP:
                    a_rotations += 1
                if UP != A[idx] and UP != B[idx]:
                    a_discarded = True
                if DOWN != A[idx] and DOWN != B[idx]:
                    b_discarded = True
            else:
                return -1
        if a_discarded and b_discarded:
            return -1
        elif a_discarded and not b_discarded:
            return b_rotations
        elif not a_discarded and b_discarded:
            return a_rotations
        else:
            return min(a_rotations, b_rotations)


